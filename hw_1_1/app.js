/* Даны числа a, b и c. Которые нужно прочитать с помощью prompt(). Вывести их сумму с помощью alert(). parseInt - функция меняет строку в число */
let a = prompt ("Введите число a")
let b = prompt ("Введите число b")
let c = prompt ("Введите число c")

let result = parseInt (a) + parseInt(b) + parseInt(c)
alert(result)
